def main_menu():
    print("\n✅ Task Manager") #app title
    inst = (f"\nCommands are as follows:\n0.Exit 1.Add 2.Update 3.View 4.Delete\nInfo: Only numeric input is allowed.\n")
    print(inst)
    
    try:
        with open(DATA_FILE, encoding='utf-8', newline='', mode='r') as f:
            if f.read().strip() == '':
                print('No task, press 1 to add new task!')
                cmd()
            else:
                fieldnames = ['Id', 'Task']
                reader = csv.DictReader(f, fieldnames=fieldnames)       
                print('All Tasks\nID Task')
                f.seek(0)
                for row in reader:
                    if row['Task'] == '':
                        continue 
                    print(f"{int(row['Id'])} - {row['Task'].title()}")
                cmd()
    except FileNotFoundError as e:
        logger.exception(e)
        print(f"No saved task found! \n Press 1 to add new task/s.")
    cmd()

main_menu()
      
     




